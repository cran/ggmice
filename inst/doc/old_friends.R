library(purrr)
library(patchwork)
# create vector with variable names
vrb <- names(dat)

## ----bwplots------------------------------------------------------------------
# original plot
mice::bwplot(imp)
# ggmice equivalent
p <- map(vrb, ~ {
  ggmice(imp, aes(x = .imp, y = .data[[.x]])) +
    geom_boxplot() +
    scale_x_discrete(drop = FALSE) +
    labs(x = "Imputation number")
})
wrap_plots(p, guides = "collect") &
  theme(legend.position = "bottom")

